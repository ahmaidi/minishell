#include "../inc/parsing.h"

void    ft_env_cmd()
{
    int i;

    i = 0;
    while (g_data.env_list[i])
    {
        ft_putstr_fd(g_data.env_list[i], 1);
        ft_putstr_fd("\n", 1);
       i++;
    }
    
}